    private function seasonalityMultiplier(int $daysAgo): float
    {
        $position = self::DEMO_TRACKING_DAYS - $daysAgo;
        $yearWave = 1 + (sin(($position / 365) * 2 * M_PI) * 0.22);
        $quarterWave = 1 + (sin(($position / 90) * 2 * M_PI) * 0.12);

        return max(0.45, $yearWave + $quarterWave);
    }

    private function weekdayMultiplier(string $dayKey): float
    {
        $weekday = (int) \gmdate('N', strtotime($dayKey . ' 00:00:00 UTC'));

        return match ($weekday) {
            6 => 1.18,
            7 => 1.12,
            5 => 1.08,
            default => 0.94,
        };
    }

    private function randomFloat(float $min, float $max): float
    {
        $random = random_int(0, 10000) / 10000;

        return $min + (($max - $min) * $random);
    }

    /**
     * @param array<string, int> $weights
     *
     * @return array<string, int>
     */
    private function distributeCountByCountry(int $count, array $weights): array
    {
        return $this->distributeWeightedCounts($count, $weights);
    }

    /**
     * @param array<int|string, int> $weights
     *
     * @return array<int|string, int>
     */
    private function distributeWeightedCounts(int $count, array $weights): array
    {
        if ($count <= 0 || $weights === []) {
            return [];
        }

        $totalWeight = array_sum($weights);

        if ($totalWeight <= 0) {
            return [];
        }

        $assigned = [];
        $remainders = [];
        $allocated = 0;

        foreach ($weights as $key => $weight) {
            $share = ($count * max(0, (int) $weight)) / $totalWeight;
            $base = (int) floor($share);
            $assigned[$key] = $base;
            $allocated += $base;
            $remainders[$key] = $share - $base;
        }

        $remaining = $count - $allocated;

        if ($remaining > 0) {
            arsort($remainders);

            foreach (array_keys($remainders) as $key) {
                if ($remaining <= 0) {
                    break;
                }

                $assigned[$key] = ($assigned[$key] ?? 0) + 1;
                $remaining--;
            }
        }

        return $assigned;
    }

    /**
     * @return list<string>
     */
    private function availableTemplateSlugs(): array
    {
        $rows = $this->frontendGalleryService->templateCatalog();
        $slugs = [];

        foreach ($rows as $row) {
            if (! is_array($row)) {
                continue;
            }

            $slug = isset($row['slug']) ? (string) $row['slug'] : '';
            $available = isset($row['available']) && (bool) $row['available'];

            if ($slug === '' || ! $available) {
                continue;
            }

            $slugs[] = $slug;
        }

        if ($slugs === []) {
            return ['basic-grid'];
        }

        return array_values(array_unique($slugs));
    }

    /**
     * @param list<int> $source
     *
     * @return list<int>
     */
    private function pickRandomIntIds(array $source, int $count): array
    {
        if ($source === []) {
            return [];
        }

        $copy = array_values(array_unique($source));
        shuffle($copy);

        return array_slice($copy, 0, max(0, min($count, count($copy))));
    }

    /**
     * @param list<string> $source
     *
     * @return list<string>
     */
    private function pickRandomStrings(array $source, int $count): array
    {
        if ($source === []) {
            return [];
        }

        $copy = array_values(array_unique($source));
        shuffle($copy);

        return array_slice($copy, 0, max(0, min($count, count($copy))));
    }

    /**
     * @param list<string> $source
     */
    private function pickRandomString(array $source, string $fallback): string
    {
        if ($source === []) {
            return $fallback;
        }

        $index = random_int(0, count($source) - 1);
        $value = isset($source[$index]) ? (string) $source[$index] : '';

        return $value !== '' ? $value : $fallback;
    }
